    if (ret){
        ESP_LOGE(GATTS_TAG, "gap register error, error code = %x", ret);
        return;
    }
    //注册service
    /*
        当调用esp_ble_gatts_app_register()注册一个应用程序Profile(Application Profile)，
        将触发ESP_GATTS_REG_EVT事件，
        除了可以完成对应profile的gatts_if的注册,
        还可以调用esp_bel_create_attr_tab()来创建profile Attributes 表
        或创建一个服务esp_ble_gatts_create_service()     
    */

    /**Finally, the Application Profiles are registered using the Application ID, which is an user-assigned number to identify each profile. In this way, multiple Application Profiles can run in one server.
     * 最后，使用应用程序ID注册应用程序配置文件，该ID是用户分配的编号，用于标识每个配置文件。这样，多个应用程序配置文件可以在一台服务器上运行。**/
